//* Assignment operator
let r = 5

//*addition Assignment
r += 1   //* r=r+1

//* subtraction Assignment
r -= 1   //* r=r-1

//* multiplication Assignment
r *= 1  //* r=r*1

//* division Assignment
r /= 1  //* r = r/1

//* remainder (modulas) Assignment
r %= 1  //* r=r%1

//* exponential Assignment
r **= 1  //* r=r**1